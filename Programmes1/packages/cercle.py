def diametre():
    n = input("Entrez le rayon du cercle: ")
    n1 = int(n) * 2
    print(f"Le diamètre de ce cercle est: {n1}")


def circonference():
    n = input("Entrez le rayon du cercle: ")
    n1 = int(n) * 2 * 3.1415
    print(f"La circonférence de ce cercle est: {n1}")


def aire():
    n = input("Entrez le rayon du cercle: ")
    n1 = int(n) * int(n) * 3.1415
    print(f"Le diamètre de ce cercle est: {n1}")
